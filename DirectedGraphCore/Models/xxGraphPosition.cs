namespace DirectedGraphCore.Models;

public class xxGraphPosition
{
    public float X { get; set; }
    public float Y { get; set; }
    public float Z { get; set; }
    public xxGraphPosition() { }
    public xxGraphPosition(float x, float y, float z)
    {
        X = x;
        Y = y;
        Z = z;
    }
}
